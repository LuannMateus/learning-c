#include <stdio.h>
#include <stdlib.h>

/*
  Operadores matemáticos para:

  Soma  -> +
  Subtração -> -
  Multiplicação -> *
  Divisão -> /
  Module -> %
*/

int main() {

  printf("\nSoma: %d", 1 + 1);
  printf("\nSubtracao: %d", 1 - 1);
  printf("\nMultiplicacao: %d", 1 * 1);
  printf("\nDivisao: %d", 1 / 1);
  printf("\nModule: %d", 1 % 1);

  return 0;
}